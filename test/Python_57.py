def monotonic(l: list):
    """Return True is list elements are monotonically increasing or decreasing.
    >>> monotonic([1, 2, 4, 20])
    True
    >>> monotonic([1, 20, 4, 10])
    False
    >>> monotonic([4, 1, 0, -10])
    True
    """
    # Let's think step by step
    # 1. Check if the list has 0 or 1 element - always monotonic
    # 2. Check if the list is increasing
    # 3. Check if the list is decreasing
    # 4. Return True if either increasing or decreasing, else False
    
    if len(l) <= 1:
        return True
    
    increasing = True
    decreasing = True
    
    for i in range(1, len(l)):
        if l[i] < l[i-1]:
            increasing = False
        if l[i] > l[i-1]:
            decreasing = False
    
    return increasing or decreasing



